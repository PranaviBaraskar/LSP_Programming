#include<stdio.h>
#include<unistd.h>
#include<stdlib.h>
#include<sys/resource.h>

//-------------------------------------*  MAIN FUNCTION  *----------------------------------------------------

extern char **environ;
int main()
{
    char **env = environ;

    printf("Environmnet of running process is : \n");

    for(; *env != NULL; env++)
    { 
        printf("%s : \n",*env);
    }
    
    return 0;
}

