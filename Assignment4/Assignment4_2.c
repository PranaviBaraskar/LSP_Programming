int main(int argc, char *argv[])
{
    if(argc != 2)
    {
        printf("Invalid parameters\n");
        return -1;
    }
    
    int Fd = 0;

    Fd = mkdir(argv[1],0777);
    if(Fd != 0)
    {
        printf("Unable to create directory\n");
        return -1;
    }
    else
    {
        printf("Successfully created the directory..\n");
    }

    
    
    return 0;
}

